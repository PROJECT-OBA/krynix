describe("Router policy namespace edge cases", () => {
  test("policy test with --help before test subcommand shows help", async () => {
    const result = await routeCommand(["policy", "--help", "test"]);
    // --help takes priority over subcommand
    expect(result.exitCode).toBe(0);
    expect(result.stdout).toContain("--policy");
  });

  test("policy with no subcommand shows namespace help", async () => {
    const result = await routeCommand(["policy"]);
    expect(result.exitCode).toBe(0);
    expect(result.stdout).toContain("policy test");
  });

  test("policy test --help shows help, not error", async () => {
    const result = await routeCommand(["policy", "test", "--help"]);
    expect(result.exitCode).toBe(0);
    expect(result.stdout).toContain("--policy");
    expect(result.stdout).toContain("--trace");
  });

  test("flags before 'test' subcommand are passed through correctly", async () => {
    // "policy --trace foo.jsonl test --policy bar.yaml" — --trace comes before test
    const tracePath = await writeTrace(tempDir, makeBasicEvents());
    const policyPath = join(tempDir, "allow.policy.yaml");
    await writeFile(policyPath, `
apiVersion: krynix.dev/v1
kind: Policy
metadata:
  name: allow-all
  version: "1.0.0"
  description: Allow everything
spec:
  scope:
    agents: ["*"]
    event_types: ["*"]
  rules:
    - id: allow-all
      description: Allow all events
      match:
        payload: []
      action: allow
      severity: info
      message: Allowed
`);

    // Flags before "test" are included in rest, then test is stripped
    const result = await routeCommand([
      "policy",
      "--trace", tracePath,
      "test",
      "--policy", policyPath,
    ]);
    // Should work — both --trace and --policy should be passed to runPolicyTest
    expect(result.exitCode).toBe(0);
    const output = JSON.parse(result.stdout);
    expect(output.verdict).toBeDefined();
  });
});

