// 1. computeTraceStats edge cases
// ---------------------------------------------------------------------------

describe("computeTraceStats edge cases", () => {
  test("negative duration: session_end before session_start yields negative ms", async () => {
    // Clock skew or bad data: end timestamp is before start
    const outputPath = join(tempDir, "trace-neg.jsonl");
    const session = await startSession({
      agentId: "test-agent",
      replaySeed: 42,
      outputPath,
    });
    // Record with an old timestamp
    await recordEvent(session, makePartial("tool_call", {
      tool_name: "file_read",
      arguments: { path: "/tmp" },
    }, "2024-01-01T00:00:00.000Z"));
    await endSession(session);

    const events = await readTrace(outputPath);
    const stats = computeTraceStats(events);

    // duration_ms computes from session_start to session_end
    // Both are generated by the session manager using Date.now()
    // so they should be close together. This tests the nominal path.
    expect(stats.duration_ms).not.toBeNull();
    expect(typeof stats.duration_ms).toBe("number");
  });

  test("trace with only non-lifecycle events yields null duration", () => {
    // A trace fragment that somehow only has tool events
    const events: TraceEvent[] = [
      {
        ...BASE,
        event_type: "tool_call",
        payload: { tool_name: "file_read", arguments: { path: "/tmp" } },
      } as unknown as TraceEvent,
    ];

    const stats = computeTraceStats(events);
    expect(stats.duration_ms).toBeNull();
    expect(stats.event_count).toBe(1);
    expect(stats.tool_call_count).toBe(1);
  });

  test("trace with checkpoint lifecycle events does not affect duration", () => {
    // checkpoint is a valid lifecycle action that should NOT be treated as start/end
    const events: TraceEvent[] = [
      {
        ...BASE,
        event_type: "lifecycle",
        timestamp: "2025-01-15T14:00:00.000Z",
        payload: { action: "session_start", context: { replay_seed: 42 } },
      } as unknown as TraceEvent,
      {
        ...BASE,
        event_id: "evt-cp",
        sequence_num: 1,
        event_type: "lifecycle",
        timestamp: "2025-01-15T14:00:30.000Z",
        payload: { action: "checkpoint" },
      } as unknown as TraceEvent,
      {
        ...BASE,
        event_id: "evt-end",
        sequence_num: 2,
        event_type: "lifecycle",
        timestamp: "2025-01-15T14:00:10.000Z",
        payload: { action: "session_end" },
      } as unknown as TraceEvent,
    ];

    const stats = computeTraceStats(events);
    // Duration should be 10s (start to end), NOT 30s (start to checkpoint)
    expect(stats.duration_ms).toBe(10000);
    expect(stats.event_type_counts["lifecycle"]).toBe(3);
  });

  test("multiple session_end events: uses last session_end for duration", () => {
    const events: TraceEvent[] = [
      {
        ...BASE,
        event_type: "lifecycle",
        timestamp: "2025-01-15T14:00:00.000Z",
        payload: { action: "session_start", context: { replay_seed: 42 } },
      } as unknown as TraceEvent,
      {
        ...BASE,
        event_id: "evt-end1",
        sequence_num: 1,
        event_type: "lifecycle",
        timestamp: "2025-01-15T14:00:05.000Z",
        payload: { action: "session_end" },
      } as unknown as TraceEvent,
      {
        ...BASE,
        event_id: "evt-end2",
        sequence_num: 2,
        event_type: "lifecycle",
        timestamp: "2025-01-15T14:00:10.000Z",
        payload: { action: "session_end" },
      } as unknown as TraceEvent,
    ];

    const stats = computeTraceStats(events);
    // Should use the LAST session_end
    expect(stats.duration_ms).toBe(10000);
  });

  test("unknown event_type is counted in event_type_counts", () => {
    const events: TraceEvent[] = [
      {
        ...BASE,
        event_type: "custom_type" as TraceEvent["event_type"],
        payload: { data: "something" },
      } as unknown as TraceEvent,
    ];

    const stats = computeTraceStats(events);
    expect(stats.event_count).toBe(1);
    // The custom type should be tracked dynamically
    expect(stats.event_type_counts["custom_type"]).toBe(1);
    // Known types should all be 0
    expect(stats.event_type_counts["tool_call"]).toBe(0);
  });

  test("llm_response with zero tokens results in total_token_usage = 0", () => {
    const events: TraceEvent[] = [
      {
        ...BASE,
        event_type: "llm_response",
        payload: {
          model: "gpt-4",
          content: "",
          usage: { prompt_tokens: 0, completion_tokens: 0 },
          finish_reason: "stop",
        },
      } as unknown as TraceEvent,
    ];

    const stats = computeTraceStats(events);
    expect(stats.total_token_usage).toBe(0);  // 0, not null (llm_response exists)
  });
});

