describe("destroySession edge cases", () => {
  test("destroy then start a new session with the same seed works", async () => {
    const path1 = join(tempDir, "trace-d1.jsonl");
    const path2 = join(tempDir, "trace-d2.jsonl");

    const session1 = await startSession({
      agentId: "test-agent",
      replaySeed: 42,
      outputPath: path1,
    });
    await destroySession(session1);

    // Starting a new session with the same seed should work
    const session2 = await startSession({
      agentId: "test-agent",
      replaySeed: 42,
      outputPath: path2,
    });

    // Both sessions get the same deterministic ID
    expect(session2.sessionId).toBe(session1.sessionId);
    await endSession(session2);
    expect(getActiveSessions()).toBe(0);
  });

  test("concurrent destroy and endSession race: first wins, second is no-op or throws", async () => {
    const path = join(tempDir, "trace-race.jsonl");
    const session = await startSession({
      agentId: "test-agent",
      replaySeed: 42,
      outputPath: path,
    });

    // Both should succeed or gracefully handle the race
    await destroySession(session);
    // endSession should now throw because session was destroyed
    await expect(endSession(session)).rejects.toThrow();
    expect(getActiveSessions()).toBe(0);
  });
});

