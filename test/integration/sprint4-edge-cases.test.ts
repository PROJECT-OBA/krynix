// 3. redactWithPatterns edge cases
// ---------------------------------------------------------------------------

describe("redactWithPatterns edge cases", () => {
  test("custom pattern matching built-in field: both apply, result is same", () => {
    // Custom pattern also matches api_key — shouldn't double-redact
    const event = {
      ...BASE,
      event_type: "tool_call" as const,
      payload: {
        tool_name: "test",
        arguments: { api_key: "sk-secret" },
      },
    } as unknown as TraceEvent;

    const result = redactWithPatterns(event, [{ pattern: "api_key" }]);
    const args = (result.payload as { arguments: Record<string, unknown> }).arguments;

    // Built-in fires first, so custom never reaches it — value should be redacted once
    expect(args["api_key"]).toMatch(/^\[REDACTED:[0-9a-f]{8}\]$/);
    // Should NOT be double-redacted (i.e., [REDACTED:...] redacted again)
    expect(args["api_key"]).not.toMatch(/^\[REDACTED:.*\[REDACTED:/);
  });

  test("already-redacted event: redacted flag preserved when no new matches", () => {
    const event = {
      ...BASE,
      redacted: true,  // already flagged
      event_type: "tool_call" as const,
      payload: {
        tool_name: "test",
        arguments: { name: "Alice" },
      },
    } as unknown as TraceEvent;

    const result = redactWithPatterns(event, [{ pattern: "^ssn$" }]);
    // No matches, but the existing redacted flag should be preserved
    expect(result.redacted).toBe(true);
  });

  test("empty string pattern matches all fields", () => {
    const event = {
      ...BASE,
      event_type: "tool_call" as const,
      payload: {
        tool_name: "test",
        arguments: { name: "Alice", age: "30" },
      },
    } as unknown as TraceEvent;

    // Empty string matches everything (regex "")
    const result = redactWithPatterns(event, [{ pattern: "" }]);
    const args = (result.payload as { arguments: Record<string, unknown> }).arguments;
    // All string values should be redacted because "" matches any string
    expect(args["name"]).toMatch(/^\[REDACTED:/);
    expect(args["age"]).toMatch(/^\[REDACTED:/);
  });

  test("deeply nested object redaction works (3+ levels)", () => {
    const event = {
      ...BASE,
      event_type: "tool_call" as const,
      payload: {
        tool_name: "test",
        arguments: {
          level1: {
            level2: {
              level3: {
                ssn: "123-45-6789",
                safe: "visible",
              },
            },
          },
        },
      },
    } as unknown as TraceEvent;

    const result = redactWithPatterns(event, [{ pattern: "^ssn$" }]);
    const args = (result.payload as { arguments: Record<string, unknown> }).arguments;
    const l1 = args["level1"] as Record<string, unknown>;
    const l2 = l1["level2"] as Record<string, unknown>;
    const l3 = l2["level3"] as Record<string, unknown>;
    expect(l3["ssn"]).toMatch(/^\[REDACTED:/);
    expect(l3["safe"]).toBe("visible");
  });

  test("null payload does not crash scanObject", () => {
    const event = {
      ...BASE,
      event_type: "lifecycle" as const,
      payload: { action: "session_start", context: { replay_seed: 42 } },
    } as unknown as TraceEvent;

    // Should not throw — lifecycle payloads have a different shape
    const result = redactWithPatterns(event, [{ pattern: "^ssn$" }]);
    expect(result).toBeDefined();
  });
});

