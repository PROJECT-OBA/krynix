describe("CLI policy test edge cases", () => {
  test("policy test with empty trace processes without crash", async () => {
    const emptyTracePath = join(tempDir, "empty.trace.jsonl");
    await writeFile(emptyTracePath, "");

    const policyPath = join(tempDir, "allow.policy.yaml");
    await writeFile(policyPath, `
apiVersion: krynix.dev/v1
kind: Policy
metadata:
  name: allow-all
  version: "1.0.0"
  description: Allow everything
spec:
  scope:
    agents: ["*"]
    event_types: ["*"]
  rules:
    - id: allow-all
      description: Allow all events
      match:
        payload: []
      action: allow
      severity: info
      message: Allowed
`);

    const result = await runPolicyTest([
      "--policy", policyPath,
      "--trace", emptyTracePath,
    ]);
    expect(result.exitCode).toBe(0);
    expect(result.result).not.toBeNull();
    expect(result.result?.verdict).toBe("pass");
    expect(result.result?.violations).toHaveLength(0);
  });

  test("--expect-verdict with correct verdict but violations still exits 0", async () => {
    // A trace that causes violations, --expect-verdict fail should succeed
    const tracePath = await writeTrace(tempDir, makeBasicEvents());
    const policyPath = join(tempDir, "deny.policy.yaml");
    await writeFile(policyPath, `
apiVersion: krynix.dev/v1
kind: Policy
metadata:
  name: deny-all
  version: "1.0.0"
  description: Deny everything
spec:
  scope:
    agents: ["*"]
    event_types: ["*"]
  rules:
    - id: deny-all
      description: Deny all events
      match:
        payload: []
      action: deny
      severity: error
      message: Denied
`);

    const result = await runPolicyTest([
      "--policy", policyPath,
      "--trace", tracePath,
      "--expect-verdict", "fail",
    ]);
    expect(result.exitCode).toBe(0);
    expect(result.result?.expectation?.match).toBe(true);
    expect(result.result?.violations.length).toBeGreaterThan(0);
  });

  test("case-sensitive verdict comparison: 'PASS' is invalid", async () => {
    const result = await runPolicyTest([
      "--policy", "p.yaml",
      "--trace", "t.jsonl",
      "--expect-verdict", "PASS",
    ]);
    expect(result.exitCode).toBe(1);
    expect(result.error).toContain("Invalid --expect-verdict");
  });

  test("nonexistent policy file returns structured error", async () => {
    const tracePath = await writeTrace(tempDir, makeBasicEvents());
    const result = await runPolicyTest([
      "--policy", "/nonexistent/policy.yaml",
      "--trace", tracePath,
    ]);
    expect(result.exitCode).toBe(1);
    expect(result.error).toContain("Failed to read policy");
  });
});
